// Реализовать четыре основные арифметические операции в виде функций
// с двумя параметрами. Обязательно использовать оператор return.

function addNumbers(x, y) {
    return x + y;
}

function subtNumbers(x, y) {
    return x - y;
}

function multNumbers(x, y) {
    return x * y;
}
function divNumbers(x, y) {
    return x / y;
}

console.log(addNumbers(5, 6)); // 11
console.log(subtNumbers(5, 6)); // -1
console.log(multNumbers(5, 6)); // 30
console.log(divNumbers(5, 6)); // 0.8333333333333334
